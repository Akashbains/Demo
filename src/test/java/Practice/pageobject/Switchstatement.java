package Practice.pageobject;

public class Switchstatement {
	
 public static void main(String[]arg) {
	 
	 
	 int number=10;
	 
	 switch(number) {
	 
	 case 1:
		 System.out.println("number is 1");
		 break;
		 
		 
	 case 2:	 
	 System.out.println("number is 2");
	 break;
	 
	 default:
		 
		 System.out.println(number);
	 
	 }
	 
 }

}
